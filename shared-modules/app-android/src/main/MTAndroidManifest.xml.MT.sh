    </application>
</manifest>
EOL

if [[ ${IS_CI} = true ]]; then
    echo "---------------------------------------------------------------------------------------------------------------"
    cat "${ANDROID_MANIFEST_FILE}"; #DEBUG
    echo "---------------------------------------------------------------------------------------------------------------"
fi

echo ">> Generating AndroidManifest.xml... DONE";